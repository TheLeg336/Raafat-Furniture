const arIdx = src.indexOf('\n  ar: {');
if (arIdx < 0) throw new Error('ar block not found');
const after = src.slice(arIdx);
const endRel = after.search(/\n\}\s*;\s*\r?\n\r?\nexport const CATEGORIES/);
if (endRel < 0) throw new Error('end of TEXTS not found');
let arBlock = after.slice(0, endRel);
const rest = after.slice(endRel);

let changed = 0;
for (const [key, value] of Object.entries(patches)) {
  const re = new RegExp(`(\\n    ${key}: )'(?:\\\\'|[^'])*'`, 'm');
  if (!re.test(arBlock)) {
    console.warn('skip missing key:', key);
    continue;
  }
  const escaped = value.replace(/\\/g, '\\\\').replace(/'/g, "\\'");
  arBlock = arBlock.replace(re, `$1'${escaped}'`);
  changed++;
}

src = src.slice(0, arIdx) + arBlock + rest;
